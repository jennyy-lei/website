function change_ending( last_char ) {
  switch (last_char) {
    case 'う':
    case 'つ':
    case 'る':  return "って";
  
    case 'む':
    case 'ぶ':
    case 'ぬ':  return "んで";
  
    case 'く':  return "いて";
  
    case 'ぐ':  return "いで";
  
    case 'す':  return "して";
  }
}

